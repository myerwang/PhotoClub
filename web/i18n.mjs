export function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : 'zh';
}

export function translate(language, key, variables = {}, fallback = '') {
  const normalized = normalizeLanguage(language);
  const template = messages[normalized][key] ?? messages.zh[key] ?? fallback ?? key;
  return template.replace(/\{(\w+)\}/g, (_match, name) => String(variables[name] ?? `{${name}}`));
}
